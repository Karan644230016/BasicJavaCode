package week2;// Start of package

public class EscapeCharacters { // Start of Class
    public static void main (String[] atgs) {   // Start of method
        // using newline (\n)
        System.out.print("Today, I went to a beach.\n"); 
        // แสดงผลข้อความแล้วขึ้นบรรทัดใหม่ ด้วย /n

        System.out.print("I see a lot of people in this\nweekend.");
        // แสดงผลข้อความแล้วขึ้นบรรทัดใหม่ ด้วย /n แล้วต่อด้วยข้อความ
        System.out.print("\n"); 
        // ขึ้นบรรทัดใหม่

        // using tab (\t)
        System.out.println("MateoCode\ttutorials"); //แสดงผลข้อความ แล้ว Tap 1 ครั้ง ต่อด้วยข้อความ
        System.out.println("is the best\ttutorial.");//แสดงผลข้อความ แล้ว Tap 1 ครั้ง ต่อด้วยข้อความ
    }// End of method

}// End of Class