package week11;// Start of package

public class ForLoop01 {// Start of Class
    public static void main(String[] args) {// Start of method
        // 3 ส่วน
        // ส่วนตัวแปรเริ่มต้น
        // เงื่อนไข
        // การเพิ่มค่า / การลดค่า
        // for คำสั่งวนซ้ำ
        for (int i = 1; i <= 10; i++) {//start of for
            //ค่าเริ่มต้นที่ 1 ถึง <=10 โดยบวกเพิ่มที่ละ 1 
            System.out.println("รอบที่ " + i + " Hello World");//แสดงผลข้อมูล
        }//end of for
    } // End of main
} // End of class