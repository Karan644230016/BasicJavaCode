package week5;// Start of package
public class LogicOperand {// Start of Class
    public static void main(String[] args) {// Start of method
        
        int a = 10;//ประกาศชนิดตัวแปร int ตัวแปร a ค่าของตัวแปร 10
        int b = 20;//ประกาศชนิดตัวแปร int ตัวแปร b ค่าของตัวแปร 20

        boolean c = (a<b)||(a>=b); //ประกาศชนิดของตัวแปร boolean ตัวแปร c    || หรือ
        System.out.println(!c);// ! ค่าตรงข้าม

    } // End of main
} // End of class
