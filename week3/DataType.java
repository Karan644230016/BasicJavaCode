package week3;// Start of package
public class DataType {// Start of Class
    public static void main(String[] args) {// Start of method
        int x = 5; // ประกาศตัวแปร x เป็นจำนวนเต็ม และกำหนดค่า x = 5
        double y = 3.1000;  // ประกาศตัวแปร y เป็นเลขทศนิยม และกำหนดค่า y = 3.1000
        double z =0;  // ประกาศตัวแปร z เป็นเลข และกำหนดค่า z = 0
        z = y + x; // นำตัวแปร y บวกกับตัวแปร x มาอยู่ในตัวแปร z
        System.out.println("z="+z);//แสดงผลข้อความ z= ตามข้อมูลในตัวแปร z
    } // End of main
} // End of class